interface SocialItem {
    platform: string;
    url: string;
    order: number;
}

function getPlatformColor(platform: string): string {
    switch (platform) {
        case 'facebook': return '#1877F2';
        case 'x': return '#000';
        case 'whatsapp': return '#25D366';
        case 'telegram': return '#26A5E4';
        case 'instagram': return '#E4405F';
        case 'linkedin': return '#0A66C2';
        case 'signal': return '#3A76F0';
        default: return '#64748b';
    }
}

export function FloatingSocialBar() {
    const pageProps = usePage().props as { socialNetworks?: SocialItem[] };
    const socialNetworks = Array.isArray(pageProps?.socialNetworks) ? pageProps.socialNetworks : [];
    const { __ } = useTranslate();
    return (
        <div className="fixed right-4 top-1/2 z-[100] -translate-y-1/2">
            <DropdownMenu>
                <DropdownMenuTrigger asChild>
                    <button
                        type="button"
                        className="flex h-12 w-12 items-center justify-center rounded-full bg-primary text-primary-foreground shadow-lg transition-transform hover:scale-105 focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2"
                        aria-label={__('Support and social networks')}
                    >
                        <Headphones className="h-6 w-6" />
                    </button>
                </DropdownMenuTrigger>
                <DropdownMenuContent side="left" align="center" className="min-w-[12rem]">
                    <DropdownMenuLabel>{__('Social Networks')}</DropdownMenuLabel>
                    {socialNetworks.length === 0 ? (
                        <DropdownMenuItem disabled className="text-muted-foreground">
                            {__('No links configured')}
                        </DropdownMenuItem>
                    ) : (
                        socialNetworks.map((item) => {
                            const icon = ICONS[item.platform];
                            if (!icon) return null;
                            return (
                                <DropdownMenuItem key={`${item.platform}-${item.order}`} asChild>
                                    <a
                                        href={item.url}
                                        target="_blank"
                                        rel="noopener noreferrer"
                                        className="flex cursor-pointer items-center gap-2"
                                    >
                                        <span
                                            className="flex h-7 w-7 shrink-0 items-center justify-center rounded text-white"
                                            style={{ backgroundColor: getPlatformColor(item.platform) }}
                                        >
                                            <svg className="h-3.5 w-3.5" viewBox="0 0 24 24" fill="currentColor" aria-hidden>
                                                <path d={icon.path} />
                                            </svg>
                                        </span>
                                        {icon.label}
                                    </a>
                                </DropdownMenuItem>
                            );
                        })
                    )}
                </DropdownMenuContent>
            </DropdownMenu>
        </div>
    );
}
